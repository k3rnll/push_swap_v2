#include "../include/push_swap.h"

void	up_stack(int *arg)
{
	int	i;

	i = 0;
	while (i < BUFF - 1)
	{
		arg[i] = arg[i + 1];
		i++;
	}
}

void	down_stack(int *arg)
{
	int	i;

	i = BUFF - 1;
	while (i > 0)
	{
		arg[i] = arg[i - 1];
		i--;
	}
}
